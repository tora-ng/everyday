
public class Oddnum {

	public static void main(String[] args) {

		int num = 1;
		if ((num % 2)==0) {
			 System.out.println("해당 값은 짝수입니다");
		} else {
			 System.out.println("해당 값은 홀수입니다");
			 
		}		 

	}

}

/*
int 변수 선언 후 임의로 값을 넣음.
해당 값이 짝수 이면 "짝수 입니다" 출력
해당 값이 홀수 이면 "홀수 입니다" 출력
*/