package java_variable;

public class Variable2 {

	public static void main(String[] args) {

		Double num1;
		Double num2;
		Double result;

		num1 = 3.14;
		num2 = 5.14;
		result = num1 * num2;
		System.out.println(result);

		/*
		 * double x = 3.14; double y = 5.14; double result = x * y;
		 * System.out.println(result);
		 */
	}

}

